<h3>Syarat & Ketentuan</h3>

<p>
Dengan menggunakan layanan Tokoonline, pengguna dianggap
telah membaca dan menyetujui seluruh syarat dan ketentuan.
</p>

<ul>
<li>Harga dapat berubah sewaktu-waktu.</li>
<li>Pesanan diproses setelah pembayaran diterima.</li>
<li>Pembatalan mengikuti kebijakan toko.</li>
<li>Pelanggaran terhadap ketentuan dapat menyebabkan akun diblokir.</li>
</ul>